    function tBackend(text) {
      const s = String(text == null ? "" : text);
      if (!s) return s;
      if (getLang() === "zh") return s;
      const hit = BACKEND_ZH_EN[s];
      if (hit) return hit;
      for (const k in BACKEND_ZH_EN) {
        if (s.indexOf(k) === 0) return BACKEND_ZH_EN[k] + s.slice(k.length);
      }
      return s;
    }
    function applyStatic(root) {
      const r = root || document;
      r.querySelectorAll("[data-i18n]").forEach((el) => {
        el.textContent = t(el.getAttribute("data-i18n") || "");
      });
      r.querySelectorAll("[data-i18n-title]").forEach((el) => {
        el.setAttribute("title", t(el.getAttribute("data-i18n-title") || ""));
      });
      r.querySelectorAll("[data-i18n-tip]").forEach((el) => {
        el.setAttribute("data-tip", t(el.getAttribute("data-i18n-tip") || ""));
      });
      r.querySelectorAll("[data-i18n-ph]").forEach((el) => {
        const phv = t(el.getAttribute("data-i18n-ph") || "");
        el.setAttribute("placeholder", phv);
        el.setAttribute("data-placeholder", phv);
      });
      document.documentElement.setAttribute("lang", getLang());
    }
    function mountSwitcher(container) {
      if (!container) return;
      const cur = getLang();
      container.innerHTML = "";
      container.setAttribute("title", t("lang.switch.title"));
      [["en", "EN"], ["zh", "中文"]].forEach((pair) => {
        const b = document.createElement("button");
        b.className = "lang-btn" + (cur === pair[0] ? " active" : "");
        b.textContent = pair[1];
        b.addEventListener("click", () => {
          setLang(pair[0]);
        });
        container.appendChild(b);
      });
    }
